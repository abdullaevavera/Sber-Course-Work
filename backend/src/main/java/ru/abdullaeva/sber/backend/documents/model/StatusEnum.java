package ru.abdullaeva.sber.backend.documents.model;

/**
 * Коды статусов документа
 */
public enum StatusEnum {

    /**
     * Статус для созданного документа "Новый".
     */
    NEW,

    /**
     * Статус документа в процессе обработки "В обработке".
     */
    IN_PROCESS,

    /**
     * Статус обработанного документа "Отклонен".
     */
    DECLINED,

    /**
     * Статус обработанного документа "Принят".
     */
    ACCEPTED
}
